{
public:
	int x;
	int y;
	bool start;

	void Start();

	Player() : x(0), y(0), start(false) { };

	int move(char c);
};

struct Hurdle
{
	int x;
	int y;
};

void Player::Start()
{
	start = true;
}

int Player::move(char c)
{
	if (c == 'w')
	{
		return y--;
	}
	else if (c == 'a')
	{
		return x--;
	}
	else if (c == 's')
	{
		return y++;
	}
	else if (c == 'd')
	{
		return x++;
	}
}

